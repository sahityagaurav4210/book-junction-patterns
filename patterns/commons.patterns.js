const Commons = Object.freeze({
  name: /^[A-Za-z\s.]{2,64}$/,
  phone: /^[+\d]{10,15}$/,
  email: /^[a-zA-Z._0-9]{2,}@[a-z]{3,32}\.[a-z.]{2,10}$/,
  password: /^(?=.*\d)(?=.*[a-z])(?=.*[A-Z])(?=.*[a-zA-Z])(?=.*[\W_]).{8,32}$/,
  dob: /^(\d{4}|([0-2]?[0-9]|[0-3]?[0-1]))(\-|\/)(([0-1]?[0-2]|[0]?[0-9]))(\-|\/)(\d{4}|([0-2]?[0-9]|[0-3]?[0-1]))$/,
  url: /^https?:\/\/(?:www\.)?([-a-zA-Z0-9@:%._\+~#=]{2,256}\.[a-z]{2,6}\b)*(\/[\/\d\w\.-]*)*(?:[\?])*(.+)*$/,
});

module.exports = Commons;
